inline int8_t Font5x7_GetIndex(char c) {
  // Extended characters (0x01-0x1F)
  if (c == 0x01) return 0;

  // ASCII 0x20-0x7E (space to '~')
  if (c >= 0x20 && c <= 0x7E) return c - 0x20 + 1;

  return -1;
}

